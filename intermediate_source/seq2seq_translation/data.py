def indexesFromSentence2(lang, sentence):
    return [lang.word2index[word] if word in lang.word2index else UNK_token
            for word in sentence.split(' ')] + [EOS_token]


def zeroPadding(l, fillvalue=PAD_token):
    return list(itertools.zip_longest(*l, fillvalue=fillvalue))

def binaryMatrix(l, value=PAD_token):
    m = []
    for i, seq in enumerate(l):
        m.append([])
        for token in seq:
            if token == value:
                m[i].append(0)
            else:
                m[i].append(1)
    return m

# Returns padded input sequence tensor and lengths
def inputVar(l, voc):
    indexes_batch = [indexesFromSentence2(voc, sentence) for sentence in l]
    lengths = torch.tensor([len(indexes) for indexes in indexes_batch])
    padList = zeroPadding(indexes_batch)
    padVar = torch.LongTensor(padList)
    return padVar, lengths

def tensor_with_mask(sentences, voc):
    indexes_batch = [indexesFromSentence2(voc, sentence) for sentence in sentences]
    padList = zeroPadding(indexes_batch)
    mask_matrix = []
    for pad in padList:
        mask = [True if idx == PAD_token else False for idx in pad]
        mask_matrix.append(mask)
    padVar = torch.LongTensor(padList)
    mask_tensor = torch.BoolTensor(mask_matrix)
    return padVar, mask_tensor


# Returns padded target sequence tensor, padding mask, and max target length
def outputVar(l, voc):
    indexes_batch = [indexesFromSentence2(voc, sentence) for sentence in l]
    max_target_len = max([len(indexes) for indexes in indexes_batch])
    padList = zeroPadding(indexes_batch)
    mask = binaryMatrix(padList)
    mask = torch.BoolTensor(mask)
    padVar = torch.LongTensor(padList)
    return padVar, mask, max_target_len

# Returns all items for a given batch of pairs
def batch2TrainData(src_voc, tgt_voc, pair_batch):
    pair_batch.sort(key=lambda x: len(x[0].split(" ")), reverse=True)
    input_batch, output_batch = [], []
    for pair in pair_batch:
        input_batch.append(pair[0])
        output_batch.append(pair[1])
    inp, lengths = inputVar(input_batch, src_voc)
    output, mask, max_target_len = outputVar(output_batch, tgt_voc)
    return inp, lengths, output, mask, max_target_len


def batch_to_transformer_data(src_voc, tgt_voc, pair_batch):
    pair_batch.sort(key=lambda x: len(x[0].split(" ")), reverse=True)
    input_batch, output_batch = [], []
    for pair in pair_batch:
        input_batch.append(pair[0])
        output_batch.append(pair[1])
    src_tensor, src_pad_mask = tensor_with_mask(input_batch, src_voc)
    tgt_tensor, tgt_pad_mask = tensor_with_mask(output_batch, tgt_voc)
    mem_pad_mask = src_pad_mask.clone()
    return src_tensor, tgt_tensor, src_pad_mask, tgt_pad_mask, mem_pad_mask


def create_batches(pairs, batch_size):
    random.shuffle(pairs)
    return batch(pairs, batch_size)


def batch(iterable, n=1):
    l = len(iterable)
    for ndx in range(0, l, n):
        yield iterable[ndx:min(ndx + n, l)]